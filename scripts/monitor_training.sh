#!/bin/bash

# 训练进度监控脚本

LOG_FILE="assets/training_output.log"

echo "========================================="
echo "训练进度监控"
echo "========================================="
echo "按 Ctrl+C 停止监控"
echo ""

while true; do
    clear
    echo "========================================="
    echo "训练进度监控"
    echo "========================================="
    echo "更新时间: $(date '+%Y-%m-%d %H:%M:%S')"
    echo ""
    echo "最近 30 行日志:"
    echo "========================================="
    tail -30 "$LOG_FILE"
    echo ""
    echo "========================================="
    echo "按 Ctrl+C 停止监控"
    echo "========================================="
    sleep 10
done
